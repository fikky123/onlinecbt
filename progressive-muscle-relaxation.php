		<h3><b>Progressive Muscle Relaxation</b></h3>
        <h4><b>Definitions</b></h4>
		<p align="justify"> Progressive muscle relaxation involves a two-step process in which you systematically
			tense and relax different muscle groups in the body. Frequently we are unaware that we are
			holding our bodies in tension until it becomes very painful. This technique teaches you to
			become very familiar and aware of how tension and complete relaxation feels in different parts
			of the body. It is designed to teach you to recognize the first signs of muscular tension that
			relaxing. As your body relaxes, so will your mind.</p>
        <hr class="w3-clear" id="basicprocedure">
        <h4><b>Basic procedure: </b></h4>
		<ul>
        <li> Loosen your clothing, take off your shoes and get in a comfortable position. You 
			 can be lying down or in a chair with your head supported.</li>
		<li> Take a few slow deep breaths and relax.</li>
		<li> Each muscle or muscle grouping is tensed for five to seven seconds then relaxed for
			 twenty to thirty seconds.</li>
		<li> It is helpful if you use relaxation expressions as you release the tension, for example:</li>
		<p><i> Let go of the tension.</i></p>
		<p><i> Release the tension ---- I am feeling calm and rested.</i></p>
		<p><i> Relax the muscles.</i></p>
		<p><i> Let go more and more</i></p>
		</ul>
		<hr class="w3-clear" id="longform">
		<h4><b>Long Form: </b></h4>
		<ol><li> Get in a comfortable position and relax. Now clench your right fist, tighter and tighter.
				 Keep it clenched and notice the tension in your fist, hand and forearm. Now relax. Feel
				 the looseness in your right hand and notice how different it feels. Repeat the entire
				 procedure with your left hand, then both hands at the same time. Always notice the
				 different feeling between tension and relaxation.</li>
			<li> Now bend your elbows and tense your biceps. Tense them as hard as you can and
				 notice the feeling of tautness. Relax and straighten your arms, noticing how it feels as
				 the muscles relax.</li>
			<li> Move your attention to your head and wrinkle your forehead as tight as you can. Frown
				 and notice the feeling the strain throughout your forehead. Hold for a few seconds then relax.
				 Imagine your entire forehead and scalp becoming smooth and at rest.</li>
			<li> Close your eyes and squint them as tight as you can. Hold for a few seconds then relax
				 and feel the difference.</li>
			<li> Clench your jaw and bite hard. Hold, then relax your jaw. When the jaw is completely
				 relaxed, your lips will be slightly parted. Let yourself really appreciate the contrast
				 between tension and relaxation.</li>
			<li> Press your tongue against the roof of your mouth until you feel an ache in the back of
				 your mouth. Relax.</li>
			<li> Purse your lips into an "O" shape. Hold, then relax. Notice that your forehead, scalp,
				 eyes, jaw, and tongue are all completely relaxed.</li>
			<li> Press your head back as far as it can comfortably go, then observe the tension. Roll it to
				 the right and feel the changing locus of stress. Roll it to the left. Straighten your head
				 and bring it forward, press your chin against your chest. Feel the tension in your throat
				 and the back of your neck. Relax, allowing your head to return to a comfortable
				 position. Let the relaxation deepen.</li>
			<li> Shrug your shoulders. Keep the tension as you hunch your head down between your
				 shoulders. Relax your shoulders. Drop them back and feel the release of tension
				 spreading through your neck, throat, and shoulders.</li>
			<p><b><i><center> Give your entire body a chance to relax. Feel the comfort and the heaviness.</center></i></b></p>
			<li> Breathe in and fill your lungs completely. Hold your breath and notice the tension.
				 Exhale and let the air hiss out. Breathe slowly and gently. Repeat this several times and
				 notice the tension draining from your body as you exhale.</li>
			<li> Tighten your stomach and hold. Relax.</li>
			<li> Arch your back and notice the tension in your lower back. Try to isolate the muscles and
				 keep the rest of your body as relaxed as possible. Now relax.</li>
			<li> Tighten your buttocks and thighs. Flex your thighs by pressing down your heels as hard
				 as you can. Relax and feel the difference.</li>
			<li> Curl your toes downward, making your calves tense. Study the tension. Relax.</li>
			<li> Bend your toes toward your shins. Relax again.</li>
			</ol>
